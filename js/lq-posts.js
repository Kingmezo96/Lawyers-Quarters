    function escapeHtml(value) {
        return String(value || "").replace(/[&<>"']/g, function (character) {
            return {
                "&": "&amp;",
                "<": "&lt;",
                ">": "&gt;",
                '"': "&quot;",
                "'": "&#39;"
            }[character];
        });
    }

    function postUrl(post) {
        return "post.html?slug=" + encodeURIComponent(post.slug);
    }

    function getPostBySlug(slug) {
        return posts.find(function (post) {
            return post.slug === slug;
        }) || posts[0];
    }

    function cardTemplate(post) {
        return [
            '<article class="lq-post-card" data-story data-category="' + escapeHtml(post.category) + '" data-title="' + escapeHtml(post.title) + '">',
            '  <a class="lq-post-card-image" href="' + postUrl(post) + '"><img src="' + escapeHtml(post.image) + '" alt="' + escapeHtml(post.alt) + '"></a>',
            '  <div class="lq-post-card-body">',
            '    <span>' + escapeHtml(post.kicker) + '</span>',
            '    <h3><a href="' + postUrl(post) + '">' + escapeHtml(post.title) + '</a></h3>',
            '    <p>' + escapeHtml(post.excerpt) + '</p>',
            '    <div class="lq-post-meta"><small><i class="fa fa-calendar"></i> ' + escapeHtml(post.date) + '</small><small><i class="fa fa-clock"></i> ' + escapeHtml(post.readTime) + '</small></div>',
            '  </div>',
            '</article>'
        ].join("");
    }

    function renderPostList() {
        var list = document.getElementById("lq-post-list");
        if (!list) return;

        var count = document.getElementById("lq-post-count");
        var search = document.getElementById("lq-post-search");
        var categoryWrap = document.getElementById("lq-post-categories");

        var activeCategory = "All";
        var categories = ["All"].concat(Array.from(new Set(posts.map(function (post) { return post.category; }))));

        function renderCategories() {
            if (!categoryWrap) return;
            categoryWrap.innerHTML = categories.map(function (category) {
                return '<button type="button" class="' + (category === activeCategory ? "active" : "") + '" data-category="' + escapeHtml(category) + '">' + escapeHtml(category) + '</button>';
            }).join("");
            categoryWrap.querySelectorAll("button").forEach(function (button) {
                button.addEventListener("click", function () {
                    activeCategory = button.dataset.category;
                    render();
                    renderCategories();
                });
            });
        }

        function render() {
            var query = search ? search.value.trim().toLowerCase() : "";
            var filtered = posts.filter(function (post) {
                var haystack = [post.title, post.category, post.excerpt, post.tags.join(" ")].join(" ").toLowerCase();
                var categoryMatch = activeCategory === "All" || post.category === activeCategory;
                return categoryMatch && (!query || haystack.indexOf(query) >= 0);
            });
            list.innerHTML = filtered.length ? filtered.map(cardTemplate).join("") : '<div class="lq-empty-state">No stories matched your filter. Try another keyword or category.</div>';
            if (count) count.textContent = "Showing " + filtered.length + " of " + posts.length + " stories";
        }

        if (search) search.addEventListener("input", render);
        renderCategories();
        render();

        var featured = document.getElementById("lq-featured-post");
        if (featured) featured.innerHTML = cardTemplate(posts[0]);
    }

